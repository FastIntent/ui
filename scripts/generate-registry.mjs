import fs from 'fs';
import path from 'path';
import { fileURLToPath } from 'url';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const componentsDir = path.resolve(__dirname, '../src/components');
const registryFile = path.resolve(__dirname, '../src/registry.ts');

async function generateRegistry() {
  console.log('🔍 Scanning components for .meta static property...');
  
  const componentImports = [];
  const entries = new Map(); // Mapa para deduplicar por 'type'

  const getFiles = (dir, allFiles = []) => {
    const files = fs.readdirSync(dir);
    for (const file of files) {
      const name = path.join(dir, file);
      if (fs.statSync(name).isDirectory()) {
         getFiles(name, allFiles);
      } else {
         allFiles.push(path.relative(componentsDir, name));
      }
    }
    return allFiles;
  };

  const files = getFiles(componentsDir);
  console.log(`📂 Total files found in ${componentsDir}: ${files.length}`);
  
  for (const file of files) {
    if (file.endsWith('index.ts') || (file.endsWith('.tsx') && !file.includes('test'))) {
      const fullPath = path.join(componentsDir, file);
      // ... rest of logic
      const relativePath = './components/' + file.replace(/\\/g, '/').replace('.tsx', '').replace('.ts', '');
      
      try {
        const modulePath = 'file://' + fullPath;
        const module = await import(modulePath);
        
        let foundInFile = false;
        for (const [name, exported] of Object.entries(module)) {
          if (exported && (typeof exported === 'function' || typeof exported === 'object')) {
            const meta = exported.meta;
            if (meta) {
              foundInFile = true;
              const type = meta.type || name.toLowerCase();
              if (entries.has(type)) continue;

              const safeIdentifier = (name === 'default' ? meta.name : name).replace(/[^a-zA-Z0-9]/g, '');
              console.log(`✨ Found component: ${safeIdentifier} (type: ${type}) in ${file}`);
              
              const importLine = name === 'default' 
                ? `import { default as ${safeIdentifier} } from '${relativePath}';`
                : `import { ${name} } from '${relativePath}';`;

              componentImports.push(importLine);
              entries.set(type, `  "${type}": { exportName: "${safeIdentifier}", meta: (${safeIdentifier} as any).meta }`);
            }
          }
        }
        if (!foundInFile) console.log(`⏩ Skipping ${file}: No .meta found in exports.`);
      } catch (e) {
        console.error(`❌ Error importing ${file}:`, e);
      }
    }
  }

  const content = `/**
 * ⚙️ AUTO-GENERATED REGISTRY
 * DO NOT EDIT THIS FILE MANUALLY. Updated by scripts/generate-registry.mjs
 */

${[...new Set(componentImports)].join('\n')}

export interface RemoteComponentProp {
  name: string;
  label: string;
  type: "string" | "number" | "boolean" | "color" | "select";
  options?: string[]; 
  defaultValue?: any;
}

export interface RemoteComponentMeta {
  type: string;
  name: string;
  version: string;
  description?: string;
  category?: string;
  isPremium?: boolean;
  isSlot?: boolean;
  parentConstraint?: string | string[];
  propControls: RemoteComponentProp[];
  dependencies?: Record<string, string>;
}

const componentRegistry: Record<string, { exportName: string; meta: RemoteComponentMeta }> = {
${[...entries.values()].join(',\n')}
};

export const getSafeCatalog = (pkgName: string, baseUrl: string = '/api/remote-asset') => {
  return Object.entries(componentRegistry).map(([regType, comp]: [string, any]) => {
    const meta = comp.meta || {};
    const isLocalApi = baseUrl.includes('/api/remote-asset');
    
    const finalType = meta.type || regType;

    const url = isLocalApi 
      ? \`\${baseUrl}?pkg=\${encodeURIComponent(pkgName)}&comp=\${encodeURIComponent(finalType)}&v=\${meta.version}\`
      : \`\${baseUrl}/\${encodeURIComponent(pkgName)}/\${meta.version}/\${finalType}.js\`;

    return {
      type: finalType,
      ...meta,
      url,
      importPath: pkgName,
      exportName: comp.exportName,
      isPremium: meta.isPremium === true
    };
  });
};
`;

  fs.writeFileSync(registryFile, content);
  console.log('✅ Registry auto-generated (CLEAN) successfully!');
}

generateRegistry();
